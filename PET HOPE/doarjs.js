document.addEventListener("DOMContentLoaded", function () {
    const linksansao = document.getElementById("sansao");
    const miniMenu = document.getElementById("mini-menu");

    

    linksansao.addEventListener("click", function (event) {
        event.preventDefault();

        if (miniMenu.classList.contains("hidden")) {
           
            miniMenu.classList.remove("hidden");
            miniMenu.classList.add("slide-in");
        } else {
           
            miniMenu.classList.add("hidden");
            miniMenu.classList.remove("slide-in");

        }
    });
});



